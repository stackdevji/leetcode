func getRangeCount(nums []int, start, end int) int {
	count := 0
	for i:= 0; i < len(nums); i++ {
		if nums[i] >= start && nums[i] <= end {
			count++
		}
	}
	return count
}

func findDuplicate(nums []int) int {
	l := len(nums)
	if l <= 0 {
		return -1
	}

	start, end := 1, l - 1 // ä¸ºä»€ä¹ˆè¦l-1, å› ä¸ºlé•¿åº¦ä¸º(n+1), åˆ¤æ–­åŒºé—´ç”¨çš„æ˜¯ 1-n
	for start <= end {
		mid := (start + end) / 2
		count := getRangeCount(nums, start, mid)

		if start == end && count > 1 {
			return start // å¯¹åº”é‡å¤çš„n
		}

		if count > mid - start + 1 {
			end = mid
		} else {
			start = mid + 1
		}
	}

	return -1
}
