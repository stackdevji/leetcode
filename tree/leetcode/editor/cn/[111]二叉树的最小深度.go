/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func minDepth(root *TreeNode) int {
	if root == nil {
		return 0
	}

	left := minDepth(root.Left)
	right := minDepth(root.Right)

	minLeve := 0
	if left > right {
		if right != 0 {
			minLeve = right
		} else {
			minLeve = left
		}
	} else {
		if left != 0 {
			minLeve = left
		} else {
			minLeve = right
		}
	}
	return minLeve + 1
}
